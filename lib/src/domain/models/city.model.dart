class CityModel {
  final int id;
  final String label;
  final String name;
  final String state;
  final String country;
  final String countryShort;
  final String lat;
  final String long;

  CityModel({
    required this.id,
    required this.label,
    required this.name,
    required this.state,
    required this.country,
    required this.countryShort,
    required this.lat,
    required this.long,
  });
}
